// Properties - of Different Types of Buttons in UI Design
// Disabled Button – Grayed out, not clickable
// Loading Button – Shows spinner while action in progress

// 1. Basic Buttons - These are the foundation buttons used everywhere.
// Fill Button – Main action (filled, prominent)
// Text Button – Minimal button, just text
// Icon Button – Only icon, no background
// Rounded Icon Button – Circular button with icon (e.g., FAB style)

// 2. State-Based Buttons - Buttons that adapt to different states.
// Toggle Button – On/off state
// Checkbox Button – Mimics checkbox behavior
// Radio Button – Single selection among a group
// Switch Button – Like toggle but modern switch style
// Active / Inactive Button – Highlighted vs normal state

// 3. Utility / Functional Buttons - Buttons used for specific functions in apps.
// Back Button – Navigation back
// Close / Cancel Button – Dismiss dialogs or modals
// Submit Button – Form submission
// Delete / Trash Button – Delete action
// Edit / Pencil Button – Edit item
// Share Button – Trigger share actions
// Download Button – Download files
// Favorite / Like Button – Heart or star
// Search Button – Magnifier icon

// 4. Advanced / Compound Buttons - Buttons with icons + text, badges, or more interactivity.
// Icon + Text Button – Icon left/right of label
// Badge Button – Button with notification count
// Dropdown Button – Opens menu / dropdown
// Segmented Button – Multiple choices in one row
// FAB (Floating Action Button) – Primary action floating
// Speed Dial FAB – FAB with multiple options expanding
// Social Buttons – Login with Google, Facebook, etc.
// Upload / Camera Button – Trigger file or camera input
// Custom Shape Button – For brand-specific styling
